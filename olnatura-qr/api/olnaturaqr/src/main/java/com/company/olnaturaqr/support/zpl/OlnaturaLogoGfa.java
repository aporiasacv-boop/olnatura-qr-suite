    /**
     * Returns ZPL ^GFA for a white filled rectangle (all zeros = white in 1-bit).
     */
    public static String whiteRectGfa(int size) {
        int bpr = (size + 7) / 8;
        int total = bpr * size;
        return String.format("^GFA,%d,%d,%d,%s", total, bpr, bpr, "0".repeat(total * 2));
    }

    /**
     * Returns ZPL ^GFA for a small 40x40 logo overlay (subsampled from original).
     */
    public static String smallOverlayGfa() {
        int bprOut = (OUT_SIZE + 7) / 8;
        int totalOut = bprOut * OUT_SIZE;
        byte[] out = new byte[totalOut];
        double sx = (double) SRC_W / OUT_SIZE;
        double sy = (double) SRC_H / OUT_SIZE;
        for (int oy = 0; oy < OUT_SIZE; oy++) {
            int sy0 = (int) (oy * sy);
            if (sy0 >= SRC_H) sy0 = SRC_H - 1;
            for (int ox = 0; ox < OUT_SIZE; ox++) {
                int sx0 = (int) (ox * sx);
                if (sx0 >= SRC_W) sx0 = SRC_W - 1;
                boolean black = getPixel(RAW_HEX, sx0, sy0);
                if (black) {
                    int idx = oy * bprOut + (ox / 8);
                    int bit = 7 - (ox % 8);
                    out[idx] |= (byte) (1 << bit);
                }
            }
        }
        StringBuilder hex = new StringBuilder(totalOut * 2);
        for (byte b : out) hex.append(String.format("%02X", b & 0xFF));
        return String.format("^GFA,%d,%d,%d,%s", totalOut, bprOut, bprOut, hex);
    }

    private static boolean getPixel(String hex, int x, int y) {
        if (x < 0 || x >= SRC_W || y < 0 || y >= SRC_H) return false;
        int byteIdx = y * SRC_BPR + (x / 8);
        int charIdx = byteIdx * 2;
        if (charIdx + 2 > hex.length()) return false;
        int b = Integer.parseInt(hex.substring(charIdx, charIdx + 2), 16);
        int bit = 7 - (x % 8);
        return ((b >> bit) & 1) == 1;
    }
